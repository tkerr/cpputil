/******************************************************************************
 * Public functions.
 ******************************************************************************/
 
/**************************************
 * checksum
 **************************************/
char checksum(const void* pData, size_t len)
{
    char* ptr = (char*) pData;
    char  sum = 0;
    do {sum += *ptr++;} while (--len);
    return ~sum + 1;
}


